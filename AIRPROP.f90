! THIS SUBROUTINE GENERATES HYDROGEN THERMODYNAMIC PROPERTIES AT A
! GIVEN PRESSURE AND TEMPERATURE
!
SUBROUTINE AIRPROP(P, TL, MU, K, RHO, CP)
IMPLICIT NONE
!
! VARIABLES USED IN AIRPROP
!
! P = AIR PRESSURE
! TL = LOCAL AIR TEMPERATURE
! MU = AIR VISCOSITY
! K = AIR THERMAL CONDUCTIVITY
! RHO = AIR DENSITY
! CP = AIR SPECIFIC HEAT AT CONSTANT PRESSURE
!
! DECLARE VARIABLES
!
DOUBLE PRECISION P, TL, MU, K, RHO, CP
!

!
 MU = (1.0D-6)*1.458*(TL**1.5)/(TL+110.4)
 
 K=(1.0D-3)*2.648151*(TL**1.5)/( TL+( 245.4*(10**(-12/TL)) ) )
 
 CP = 10*(0.021144)*((0.001*TL)**9)+9*(-0.45165)*((0.001*TL)**8) &
         +8*(3.83286)*((0.001*TL)**7)+7*(-15.8513)*((0.001*TL)**6) &
         +6*(26.75992)*((0.001*TL)**5)+5*(29.9455575)*((0.001*TL)**4) &
         +4*(-220.448)*((0.001*TL)**3)+3*(388.667)*((0.001*TL)**2) &
         +2*(-206.916)*((0.001*TL)**1)+1043.76;  
 
 RHO = P/(287*TL)

RETURN
!
! END AIRPROP SUBROUTINE
END

! THIS SUBROUTINE GENERATES N2 THERMODYNAMIC PROPERTIES AT A
! GIVEN PRESSURE AND TEMPERATURE
!
SUBROUTINE N2PROP(P, TL, MU, K, RHO, CP)
IMPLICIT NONE
!
! VARIABLES USED IN H2PROP
!
! P = HYDROGEN PRESSURE
! TL = LOCAL HYDROGEN TEMPERATURE
! MU = HYDROGEN VISCOSITY
! K = HYDROGEN THERMAL CONDUCTIVITY
! RHO = HYDROGEN DENSITY
! CP = HYDROGEN SPECIFIC HEAT AT CONSTANT PRESSURE
!
! DECLARE VARIABLES
!
DOUBLE PRECISION P, TL, MU, K, RHO, CP
!
! CURVE FITS TO CALCULATE PROPERTIES
!
MU = (6.5592D-7)*(TL**(0.6081))/(1.0+54.714/TL)

K = (3.3143D-4)*(TL**(0.7722))/(1.0+16.323/TL+373.72/TL/TL)

RHO = P/(296.93*TL)

CP = 29105+8614.9*( 2*1701.6/TL/( exp(1701.6/TL)-exp(-1701.6/TL) ) ) *( 2*1701.6/TL/( exp(1701.6/TL)-exp(-1701.6/TL) ) )&
+103.47*( 2*909.79/TL/( exp(909.79/TL)+exp(-909.79/TL) ) ) *( 2*909.79/TL/( exp(909.79/TL)+exp(-909.79/TL) ) )
CP=CP/28
RETURN
!
! END N2PROP SUBROUTINE
END
